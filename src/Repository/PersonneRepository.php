    public function loadUserByUsername(string $email):?Personne
    {
        $entityManager = $this->getEntityManager();

        return $entityManager->createQuery(
            'SELECT p
                FROM App\Entity\Personne p
                WHERE p.email = :query
                AND p.compteActived = true'
        )
            ->setParameter('query', $email)
            ->getOneOrNullResult();
    }
